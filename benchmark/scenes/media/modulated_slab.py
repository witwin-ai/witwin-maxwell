from __future__ import annotations

import witwin.maxwell as mw

from benchmark.models import ScenarioDefinition
from benchmark.scenes.media._common import FREQUENCY, slab_scene


FREQUENCIES = (FREQUENCY - 0.2e9, FREQUENCY, FREQUENCY + 0.2e9)


def build_scene() -> mw.Scene:
    return slab_scene(
        mw.Material(
            eps_r=4.0,
            modulation=mw.ModulationSpec(frequency=0.2e9, amplitude=0.1, phase=0.0),
            name="modulated_dielectric",
        ),
        name="modulated_slab",
        frequencies=FREQUENCIES,
        source_time=mw.CW(frequency=FREQUENCY, amplitude=1.0),
        boundary=mw.BoundarySpec.pml(num_layers=12).with_faces(
            x_low="periodic", x_high="periodic", y_low="periodic", y_high="periodic"
        ),
    )


SCENARIO = ScenarioDefinition(
    name="modulated_slab",
    description="Time-modulated permittivity slab (Tidy3D Medium + ModulationSpec export)",
    builder=build_scene,
    frequencies=FREQUENCIES,
    display_monitor="plot_field_y",
    display_component="Ex",
    run_time_factor=20.0,
    # Sidebands are generated by the modulation and have no incident source
    # spectrum to divide by. Compare their complex fields relative to the
    # carrier instead of mixing Maxwell phasors with Tidy3D's raw Hz^-1 DFT.
    normalize_source=False,
    spectral_reference_index=1,
    compare_flux=False,
)
